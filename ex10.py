# The \t adds a tab to the string
tabby_cat = "\tI'm tabbed in."
# The \n adds a new line in the string
persian_cat = "I'm split\non a line."
# the \\ adds a single backslash to the string
backslash_cat = "I'm \\ a \\ cat."

# This uses the triple-quotes to write as much as needed as well as tabs and new lines
fat_cat = """
I'll do a list:
\t* Cat Food
\t* Fishies
\t* Catnip\n\t* Grass
"""

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
